public class Person {
    String f_name;
    String sur_name;
    int credit_card_num;
    int number_of_guests;

    Person(String get_f_name, String get_sur_name, int get_credit_card_num, int get_number_of_guests){
        this.f_name = get_f_name;
        this.sur_name = get_sur_name;
        this.credit_card_num = get_credit_card_num;
        this.number_of_guests = get_number_of_guests;
    }

}
